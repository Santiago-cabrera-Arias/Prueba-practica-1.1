    private void btnIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarSesionActionPerformed

        String usuario = txtUsuario.getText();
        String contrasena = txtContrasena.getText();

        Usuario u = new Usuario(usuario, contrasena);

        controladorUsuario.create(u);

        JOptionPane.showMessageDialog(this, "usuario registrado correctamente");

    }//GEN-LAST:event_btnIniciarSesionActionPerformed

    private void btnRegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarseActionPerformed

        String usuario = txtUsuario.getText();
        String contrasena = txtContrasena.getText();

        if (usuario.isEmpty() || contrasena.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Llene los campos");

        } else {

            Usuario u = controladorUsuario.login(usuario, contrasena);

            if (u != null) {

                ventanaPrincipal.itemConyugue().setVisible(true);
                ventanaPrincipal.itemMatrimonio().setVisible(true);
                ventanaPrincipal.itemRegistrarJuez().setVisible(true);
                ventanaPrincipal.itemTestigos().setVisible(true);

                JOptionPane.showMessageDialog(this, "Sesion Inciada");

            }

        }

    }//GEN-LAST:event_btnRegistrarseActionPerformed


